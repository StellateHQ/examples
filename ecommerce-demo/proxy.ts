import { type NextRequest, NextResponse } from 'next/server';

export async function proxy(request: NextRequest) {
  if (request.cookies.has('user-ids')) return;

  const userUncached = randomUserId();
  const userCached = randomUserId();

  const response = NextResponse.next();
  response.cookies.set('user-ids', JSON.stringify({ uncached: userUncached, cached: userCached }));

  // cookies.set does not support setting the same cookie for two different paths, so append headers manually
  response.headers.append('set-cookie', `user-id=${userUncached}; Path=/uncached`);
  response.headers.append('set-cookie', `user-id=${userCached}; Path=/cached`);

  return response;
}

